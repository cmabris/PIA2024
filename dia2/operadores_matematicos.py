x = 6
y = 2

print(f"{x} más {y} es igual a {x + y}")
print(f"{x} menos {y} es igual a {x - y}")
print(f"{x} por {y} es igual a {x * y}")
print(f"{x} entre {y} es igual a {x / y}")

z = 7
# División entera
print(f"{z} dividido {y} es igual a {z // y}")

# Resto de la división
print(f"El resto de dividir {z} entre {y} es {z % y}")

# Potencia
print(f"{x} elevado a {y} es igual a {x ** y}")
print(f"{x} elevado a {3} es igual a {x ** 3}")

# Raíz cuadrada
print(f"La raíz cuadrada de {x} es igual a {x ** 0.5}")
print(f"La raiz cúbica de {x} es igual a {x ** (1/3)}")
